import math

#operasi akar dan pangkat
print("akar kuadrat dari angka 16 adalah: ", math.sqrt(16))
print("3 pangkat 2 hasilnya adalah: ", math.pow(3, 2))

#pembulatan
print("pembulatan keatas menggunakan fungsi ceiling, dari awal angka 6.7 adalah", math.ceil(6.7))
print("pembulatan kebawah menggunakan fungsi floor, dari awal angka 6.4 adalah", math.floor(6.4))


